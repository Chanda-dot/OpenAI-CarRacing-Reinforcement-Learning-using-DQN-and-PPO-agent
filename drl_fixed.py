# Redefine make_ppo_env here so this cell works after any restart
def make_ppo_env(seed=0):
    def _init():
        env = gym.make('CarRacing-v3', continuous=True, render_mode='rgb_array')
        env = Monitor(env, filename=f'{SAVE_DIR}/logs/ppo_monitor')
        env.reset(seed=seed)
        return env
    return _init

ABLATION_STEPS = 100_000
learning_rates = [1e-4, 3e-4, 7.5e-4]
ablation_results = {}

for lr in learning_rates:
    print(f'\nTraining PPO with lr={lr}...')
    env = DummyVecEnv([make_ppo_env(seed=0)])
    env = VecTransposeImage(env)
    env = VecFrameStack(env, n_stack=4)

    model = PPO(
        'CnnPolicy', env,
        learning_rate=lr,
        n_steps=1024, batch_size=64, n_epochs=10,
        gamma=0.99, gae_lambda=0.95,
        clip_range=0.15, ent_coef=0.01,
        verbose=0
    )
    model.learn(total_timesteps=ABLATION_STEPS, progress_bar=True)

    eval_env = DummyVecEnv([make_ppo_env(seed=99)])
    eval_env = VecTransposeImage(eval_env)
    eval_env = VecFrameStack(eval_env, n_stack=4)

    mean_r, std_r = evaluate_policy(model, eval_env, n_eval_episodes=5, deterministic=True)
    ablation_results[lr] = (mean_r, std_r)
    print(f'  lr={lr}: mean reward={mean_r:.2f} +/- {std_r:.2f}')
    env.close()
    eval_env.close()

