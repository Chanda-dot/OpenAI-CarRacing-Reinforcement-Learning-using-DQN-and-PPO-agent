import imageio
from IPython.display import Video, display as ipy_display
from stable_baselines3 import PPO, DQN
from stable_baselines3.common.vec_env import DummyVecEnv, VecTransposeImage, VecFrameStack

def record_agent(model, env_creation_function, is_ppo_model, video_path, n_episodes=3, fps=30):
    frames = []
    for ep in range(n_episodes):
        env = DummyVecEnv([env_creation_function(seed=ep)])
        env = VecTransposeImage(env)
        if is_ppo_model:
            env = VecFrameStack(env, n_stack=4)
        obs = env.reset()
        done = False
        ep_reward = 0
        step = 0
        while not done and step < 1000:
            frame = env.envs[0].env.render()
            if frame is not None:
                frames.append(frame)
            action, _ = model.predict(obs, deterministic=True)
            obs, reward, done_arr, info = env.step(action)
            ep_reward += reward[0]
            done = done_arr[0]
            step += 1
        print(f'Episode {ep+1}: reward={ep_reward:.2f}, steps={step}')
        env.close()
    if len(frames) == 0:
        print('WARNING: No frames captured.')
        return None
    imageio.mimwrite(video_path, frames, fps=fps, codec='libx264')
    print(f'Video saved to: {video_path}')
    return video_path

