"""## Cell 5 — Custom reward shaping wrapper (optional — comment out to use default sparse reward)"""

class ShapedRewardWrapper(gym.Wrapper):
    """
    Custom reward function:
    r(t) = alpha * r_progress - beta * r_time - gamma * r_offtrack - delta * r_speed

    alpha=1.0  : reward per new track tile visited
    beta=0.1   : timestep penalty (encourages speed)
    gamma=5.0  : off-track penalty (safety term)
    delta=0.05 : penalty for speed below v_min (anti-stall)
    """
    def __init__(self, env, alpha=1.0, beta=0.1, gamma=5.0, delta=0.05, v_min=0.1):
        super().__init__(env)
        self.alpha = alpha
        self.beta  = beta
        self.gamma = gamma
        self.delta = delta
        self.v_min = v_min

    def step(self, action):
        obs, reward, terminated, truncated, info = self.env.step(action)

        # Progress reward: use the environment's tile reward as proxy
        r_progress = max(reward, 0) * self.alpha

        # Timestep penalty
        r_time = self.beta

        # Off-track penalty: detect grass (green channel dominates in grass pixels)
        # Look at centre strip of observation
        frame = np.array(obs)
        if frame.ndim == 4:          # stacked frames — use most recent
            frame = frame[-1]
        centre = frame[60:80, 40:56] # region where track should be
        green_dom = (centre[..., 1] > centre[..., 0]) & (centre[..., 1] > centre[..., 2])
        off_track = float(green_dom.mean() > 0.5)
        r_offtrack = self.gamma * off_track

        # Speed regularisation — approximate from reward signal
        speed_proxy = max(reward, 0)
        r_speed = self.delta * max(0, self.v_min - speed_proxy)

        shaped_reward = r_progress - r_time - r_offtrack - r_speed
        return obs, shaped_reward, terminated, truncated, info

print('ShapedRewardWrapper defined.')

